<template>
	<div
		class="banner"
		:class="[
			`variant-${variant}`,
			{ 'has-link': !!link },
			{ 'link-break': linkBreak }
		]"
	>
		<div class="banner-icon">
			<component :is="getIconComponent" />
		</div>
		<div class="banner-content">
			<template v-if="!linkBreak">
				<span class="banner-text">{{ text }}</span>
				<a v-if="link" class="banner-link" :href="link">{{ linkText || 'Opsæt Checkliste' }}</a>
			</template>
			<template v-else>
				<div class="banner-text">{{ text }}</div>
				<a v-if="link" class="banner-link" :href="link">{{ linkText || 'Opsæt Checkliste' }}</a>
			</template>
		</div>
	</div>
</template>

<script setup>
import { computed } from 'vue'
import { IconAlertCircle, IconAlertTriangle, IconCircleCheck } from '@tabler/icons-vue'

const props = defineProps({
  // Banner variant
  variant: {
    type: String,
    default: 'warning',
    validator: value => ['warning', 'error', 'success'].includes(value)
  },
  // Banner text
  text: {
    type: String,
    default: 'Dette skema vil blive oprettet uden en checkliste.'
  },
  // Optional link
  link: {
    type: String,
    default: ''
  },
  // Optional link text
  linkText: {
    type: String,
    default: ''
  },
  // Whether to show the link on a new line
  linkBreak: {
    type: Boolean,
    default: false
  }
})

// Compute the icon component based on variant
const getIconComponent = computed(() => {
  switch (props.variant) {
    case 'warning':
      return IconAlertCircle
    case 'error':
      return IconAlertTriangle
    case 'success':
      return IconCircleCheck
    default:
      return IconAlertCircle
  }
})
</script>

